var HUD = function (obj) {

    this.text = obj.text;
    this.position = obj.position;
    this.scale = obj.scale;
    this.v3Color = obj.v3Color;
    this.verticalAlign = obj.verticalAlign || "default verticalAlign";
};

HUD.prototype.baseTextSize = 1;
HUD.prototype.baseHeight = 200;
HUD.prototype.textScaleFactor = 1;


